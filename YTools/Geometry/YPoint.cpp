#include <iostream>
#include "YPoint.h"
#include "math.h"

using namespace Yt;

// *************************************************************************************
YPoint::YPoint() :
	x (0),
	y (0),
	z (0),
	t (0)
{}
// *************************************************************************************
YPoint::YPoint(double xx, double yy, double zz) :
	x (xx),
	y (yy),
	z (zz),
	t (1)
{}
// *************************************************************************************
YPoint::YPoint(double xx, double yy, double zz, double tt) :
	x (xx),
	y (yy),
	z (zz),
	t (tt)
{}
// *************************************************************************************
double YPoint::centerDist() const
{
	return(sqrt(x*x + y*y + z*z));
}
// *************************************************************************************
void YPoint::print() const
{
    std::cout
	    << "("
	    << x << ", " << y << ", " << z << ", " << t
	    << ") = "
	    << centerDist()
	    << std::endl
	    ;
}
// *************************************************************************************
YPoint::~YPoint(void)
{}
